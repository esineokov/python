# Типы данных

#  Число
a = 1
print(a)

# Дробное число
b = 10.1
print(b)

# Строка
c = "строки"
print(c)

# Список
my_list = [1, 2, 3, 'список', [1, 2, 4]]
print(my_list)
# Список (2)
my_list2 = list()
my_list2.append(1)

# Словарь
my_dict = {'a': 1, 'b': 2}
print(my_dict)

# Доступ к значению словаря по ключу
print(my_dict['a'])

# Словарь (2)
my_dict2 = dict(a=1, b=2)
print(my_dict2)

# Кортеж
my_tuple = (1, 2, 3, 'список', [1, 2, 4])
print(my_tuple)

# Кортеж (2)
my_tuple2 = tuple([1, 2, 3])
print(my_tuple2)

# Добавление новой пары ключ-значение в словарь
my_dict3 = {'a': 1, 'b': 2}
my_dict3['c'] = 3
print(my_dict3)


