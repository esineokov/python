# Математические операции

a = 100
b = 20.5

# Умножение
print(a * b)

# Деление
print(a / b)

# Возведение в степень
print(a ** 2)

# Вычитание
print(a - b)

# Сложение
print(a + b)

# Деление без остатка
print(a // b)
print('-------------')

# Другие примеры
c = a * b
z = c + 10
print(c)
print(z)
